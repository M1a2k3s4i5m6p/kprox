static constexpr const char* CS_NOSTR_PRIVKEY = "__nostr__privkey";
static constexpr const char* CS_NOSTR_PUBKEY  = "__nostr__pubkey";
static constexpr const char* DEFAULT_RELAY    = "wss://relay.damus.io";
static constexpr const char* DEFAULT_CHANNEL  = "#kprox";

// ---- Credential store helpers -----------------------------------------------

bool AppNostrProx::_reloadKeys() {
    // Config always loads regardless of CredStore state
    preferences.begin("kprox_nostr", true);
    _relay   = preferences.getString("relay",   DEFAULT_RELAY);
    _channel = preferences.getString("channel", DEFAULT_CHANNEL);
    _name    = preferences.getString("name",    "");
    preferences.end();

    nostrClient.setLocalName(_name);

    if (credStoreLocked) { _keysLoaded = false; return false; }
    _privkey = credStoreGet(CS_NOSTR_PRIVKEY);
    _pubkey  = credStoreGet(CS_NOSTR_PUBKEY);
    _keysLoaded = (_privkey.length() == 64 && _pubkey.length() == 64);
    return _keysLoaded;
}

void AppNostrProx::_saveKeys() {
    credStoreSet(CS_NOSTR_PRIVKEY, _privkey);
    credStoreSet(CS_NOSTR_PUBKEY,  _pubkey);
}

void AppNostrProx::_saveConfig() {
    preferences.begin("kprox_nostr", false);
    preferences.putString("relay",   _relay);
    preferences.putString("channel", _channel);
    preferences.putString("name",    _name);
    preferences.end();
    nostrClient.setLocalName(_name);
}

