// ---- Page 0: Feed + Compose -------------------------------------------------
//
// Layout (135px tall display):
//   [0..15]    top bar      16px
//   [16..104]  feed area    89px  (~7 rows × 12px)
//   [105]      divider      1px
//   [106..120] input field  15px
//   [121..134] bottom bar   14px

static constexpr int FEED_Y      = AppNostrProx::BAR_H + 1;
static constexpr int INPUT_H     = 15;
static constexpr int INPUT_Y     = 135 - AppNostrProx::BOT_H - INPUT_H;
static constexpr int FEED_BOTTOM = INPUT_Y - 2;
static constexpr int FEED_ROW_H  = 12;

void AppNostrProx::_drawPage0() {
    auto& d = M5Cardputer.Display;
    d.fillScreen(NP_BG);
    char extra[6]; snprintf(extra, sizeof(extra), "%d", nostrClient.msgCount());
    _drawTopBar(0, extra);

    int feedRows = (FEED_BOTTOM - FEED_Y) / FEED_ROW_H;
    int total    = nostrClient.msgCount();

    // ---- Feed area ----
    if (total == 0) {
        d.setTextSize(1);
        d.setTextColor(d.color565(80, 80, 80), NP_BG);
        const char* msg;
        if (credStoreLocked)                 msg = "Unlock CredStore first";
        else if (!_keysLoaded)               msg = "No keys — go to Status (G)";
        else if (!nostrClient.isConnected()) msg = !_p0Status.isEmpty() ? _p0Status.c_str()
                                                                         : "Connecting...";
        else                                 msg = "Waiting for messages...";
        d.drawString(msg, 4, FEED_Y + 20);
    } else {
        int maxScroll = max(0, total - feedRows);
        if (_feedScrollPinned) _feedScroll = maxScroll;
        if (_feedScroll > maxScroll) _feedScroll = maxScroll;

        // _msgs[0]=oldest, display from _feedScroll (oldest at top, newest at bottom)
        int y = FEED_Y;
        for (int i = 0; i < feedRows && (_feedScroll + i) < total; i++) {
            const NostrMsg& m = nostrClient.msg(_feedScroll + i);
            d.setTextSize(1);
            char pfx[20]; snprintf(pfx, sizeof(pfx), "%s: ", m.name);
            int pfxW = d.textWidth(pfx);
            d.setTextColor(d.color565(140, 100, 200), NP_BG);
            d.drawString(pfx, 4, y);
            d.setTextColor(d.color565(210, 210, 210), NP_BG);
            int maxChars = (d.width() - pfxW - 8) / 6;
            String content = String(m.content);
            if ((int)content.length() > maxChars) content = content.substring(0, maxChars-1) + "~";
            d.drawString(content, 4 + pfxW, y);
            y += FEED_ROW_H;
        }

        if (total > feedRows) {
            char sb[10]; snprintf(sb, sizeof(sb), "%d/%d", _feedScroll + feedRows, total);
            d.setTextColor(d.color565(60,60,60), NP_BG);
            d.drawString(sb, d.width()-d.textWidth(sb)-2, FEED_Y);
        }
    }

    // ---- Divider ----
    d.drawFastHLine(0, INPUT_Y - 1, d.width(), d.color565(40, 20, 60));

    // ---- Input field ----
    bool canPost = nostrClient.isConnected() && _keysLoaded && !credStoreLocked;
    uint16_t ibg = _compEditing ? d.color565(25, 15, 50) : d.color565(16, 10, 32);
    d.fillRect(0, INPUT_Y, d.width(), INPUT_H, ibg);
    d.setTextSize(1);
    if (_compEditing) {
        int maxChars = (d.width() - 8) / 6;
        String disp = _compBuf;
        if ((int)disp.length() >= maxChars) disp = disp.substring(disp.length() - maxChars + 1);
        disp += '_';
        d.setTextColor(TFT_WHITE, ibg);
        d.drawString(disp, 4, INPUT_Y + 3);
    } else if (!_compStatus.isEmpty()) {
        d.setTextColor(_compStatusOk ? TFT_GREEN : d.color565(220,80,80), ibg);
        d.drawString(_compStatus, 4, INPUT_Y + 3);
    } else if (!nostrClient.lastRelayMsg().isEmpty()) {
        String rm = nostrClient.lastRelayMsg();
        if ((int)rm.length() > 34) rm = rm.substring(0, 33) + "~";
        uint16_t mc = rm.startsWith("REJECT") ? d.color565(220,80,80) : d.color565(140,140,100);
        d.setTextColor(mc, ibg);
        d.drawString(rm, 4, INPUT_Y + 3);
    } else {
        d.setTextColor(d.color565(60, 40, 80), ibg);
        d.drawString(canPost ? "ENTER to compose..." : (credStoreLocked ? "Unlock CredStore" : "Connect to post"), 4, INPUT_Y + 3);
    }

    if (_compEditing)
        _drawBottomBar("type  ENTER=send  ESC=cancel  DEL=del");
    else
        _drawBottomBar("ENTER=compose  up/dn=scroll  </>=page");
}

void AppNostrProx::_handlePage0(KeyInput ki) {
    int total    = nostrClient.msgCount();
    int feedRows = (FEED_BOTTOM - FEED_Y) / FEED_ROW_H;

    if (_compEditing) {
        if (ki.esc) { _compEditing = false; _compBuf = ""; _compStatus = ""; _needsRedraw = true; return; }
        if (ki.del && !_compBuf.isEmpty()) { _compBuf.remove(_compBuf.length()-1); _needsRedraw = true; return; }
        if (ki.enter) {
            if (_compBuf.isEmpty()) {
                _compEditing = false; _compStatus = ""; _needsRedraw = true; return;
            }
            _compStatus = "Signing..."; _compStatusOk = true;
            _needsRedraw = true; _drawPage0();
            if (nostrClient.publish(_privkey, _compBuf, _channel)) {
                _compStatus = "Sent!"; _compStatusOk = true;
                _compBuf = ""; _compEditing = false;
                _feedScrollPinned = true;   // auto-scroll to show own message at bottom
                _pendingRelayCheck = true;  // check relay OK/REJECT once, then stop
            } else {
                _compStatus = "Send failed"; _compStatusOk = false;
            }
            _needsRedraw = true; return;
        }
        if (ki.arrowUp) {
            if (_feedScroll > 0) { _feedScroll--; _feedScrollPinned = false; }
            _needsRedraw = true; return;
        }
        if (ki.arrowDown) {
            int maxScroll = max(0, total - feedRows);
            if (_feedScroll < maxScroll) _feedScroll++;
            if (_feedScroll >= maxScroll) _feedScrollPinned = true;
            _needsRedraw = true; return;
        }
        if (ki.ch && (int)_compBuf.length() < 280) { _compBuf += ki.ch; _compStatus = ""; _needsRedraw = true; }
        return;
    }

    if (ki.arrowLeft)  { _page = NUM_PAGES-1; _needsRedraw = true; return; }
    if (ki.arrowRight) { _page = 1;           _needsRedraw = true; return; }
    if (ki.arrowUp) {
        if (_feedScroll > 0) { _feedScroll--; _feedScrollPinned = false; }
        _needsRedraw = true; return;
    }
    if (ki.arrowDown) {
        int maxScroll = max(0, total - feedRows);
        if (_feedScroll < maxScroll) _feedScroll++;
        if (_feedScroll >= maxScroll) _feedScrollPinned = true;
        _needsRedraw = true; return;
    }

    if (ki.enter) {
        if (nostrClient.isConnected() && _keysLoaded && !credStoreLocked) {
            _compEditing = true; _compBuf = ""; _compStatus = ""; _needsRedraw = true;
        } else {
            _page = 1; _needsRedraw = true;
        }
    }
}

