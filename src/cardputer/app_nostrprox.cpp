void AppNostrProx::_handlePage1(KeyInput ki) {
    if (credStoreLocked) {
        if (ki.arrowLeft)  { _page = 0;           _needsRedraw = true; }
        if (ki.arrowRight) { _page = 2;            _needsRedraw = true; }
        return;
    }

    if (_p0GenConfirm) {
        if (ki.ch == 'y' || ki.ch == 'Y') {
            _p0GenConfirm = false;
            _p0Status = "Generating..."; _p0StatusOk = true;
            _needsRedraw = true; _drawPage1();
            String priv, pub;
            if (NostrClient::generateKeypair(priv, pub)) {
                _privkey = priv; _pubkey = pub; _keysLoaded = true;
                _saveKeys(); _p0Status = "Keys generated!"; _p0StatusOk = true;
            } else {
                _p0Status = "Key gen failed"; _p0StatusOk = false;
            }
        } else {
            _p0GenConfirm = false; _p0Status = "Cancelled"; _p0StatusOk = false;
        }
        _needsRedraw = true; return;
    }

    if (ki.arrowLeft)  { _page = 0;           _needsRedraw = true; return; }
    if (ki.arrowRight) { _page = 2;            _needsRedraw = true; return; }

    if (ki.ch == 'g' || ki.ch == 'G') {
        if (_keysLoaded) { _p0GenConfirm = true; }
        else {
            _p0Status = "Generating..."; _p0StatusOk = true;
            _needsRedraw = true; _drawPage1();
            String priv, pub;
            if (NostrClient::generateKeypair(priv, pub)) {
                _privkey = priv; _pubkey = pub; _keysLoaded = true;
                _saveKeys(); _p0Status = "Keys generated!"; _p0StatusOk = true;
            } else { _p0Status = "Key gen failed"; _p0StatusOk = false; }
        }
        _needsRedraw = true; return;
    }

    if (ki.enter) {
        if (nostrClient.isConnected()) {
            nostrClient.disconnect();
            _p0Status = "Disconnected"; _p0StatusOk = false;
        } else {
            _p0Status = "Connecting..."; _p0StatusOk = true;
            _needsRedraw = true; _drawPage1();
            if (WiFi.status() != WL_CONNECTED) {
                _p0Status = "No WiFi"; _p0StatusOk = false;
            } else if (nostrClient.connect(_relay)) {
                nostrClient.subscribe(_channel);
                _lastRefreshMs = millis();
                _p0Status = "Connected!"; _p0StatusOk = true;
                // Jump straight to feed
                _page = 0;
            } else {
                _p0Status = nostrClient.lastError(); _p0StatusOk = false;
            }
        }
        _needsRedraw = true; return;
    }
}

// ---- Page 2: Config ---------------------------------------------------------

void AppNostrProx::_drawPage2() {
    auto& d = M5Cardputer.Display;
    d.fillScreen(NP_BG);
    _drawTopBar(2);
    int y = BAR_H + 4;
    d.setTextSize(1);

    if (credStoreLocked) {
        d.setTextColor(d.color565(220,140,60), NP_BG);
        d.drawString("CredStore locked.", 4, y);
        _drawBottomBar("</> page  ESC back");
        return;
    }

    struct { const char* label; const String* val; CfgField field; } rows[CF_COUNT] = {
        { "Relay URL:", &_relay,   CF_RELAY   },
        { "Channel:",   &_channel, CF_CHANNEL },
        { "Name:",      &_name,    CF_NAME    },
    };

    for (int i = 0; i < CF_COUNT; i++) {
        bool sel  = (_cfgSel == (CfgField)i);
        bool edit = (sel && _cfgEditing);
        uint16_t rowBg = sel ? d.color565(30,10,60) : (uint16_t)NP_BG;
        if (sel) d.fillRect(0, y-1, d.width(), 26, rowBg);
        d.setTextColor(sel ? d.color565(200,160,255) : d.color565(140,140,140), rowBg);
        d.drawString(rows[i].label, 4, y); y += 10;
        uint16_t fbg = edit ? d.color565(40,20,80) : d.color565(20,15,40);
        d.fillRoundRect(4, y, d.width()-8, 13, 2, fbg);
        d.setTextColor(TFT_WHITE, fbg);
        String val = edit ? (_cfgBuf + "_") : *rows[i].val;
        if (val.isEmpty() && !edit) val = "(none)";
        if ((int)val.length() > 36) val = val.substring(val.length()-36);
        d.drawString(val, 6, y+2);
        y += 16;
    }

    if (!_cfgStatus.isEmpty()) {
        d.setTextColor(_cfgStatusOk ? TFT_GREEN : d.color565(220,80,80), NP_BG);
        d.drawString(_cfgStatus, 4, y);
    }

    if (_cfgEditing) _drawBottomBar("type  ENTER=save  ESC=cancel  DEL=del");
    else             _drawBottomBar("up/dn=row  ENTER=edit  </>=page  ESC");
}

void AppNostrProx::_handlePage2(KeyInput ki) {
    if (credStoreLocked) {
        if (ki.arrowLeft)  { _page = 1; _needsRedraw = true; }
        if (ki.arrowRight) { _page = 0; _needsRedraw = true; }
        return;
    }

    if (_cfgEditing) {
        if (ki.esc)   { _cfgEditing = false; _cfgBuf = ""; _cfgStatus = ""; _needsRedraw = true; return; }
        if (ki.del && !_cfgBuf.isEmpty()) { _cfgBuf.remove(_cfgBuf.length()-1); _needsRedraw = true; return; }
        if (ki.enter) {
            if (_cfgSel == CF_RELAY)   _relay   = _cfgBuf.isEmpty() ? String(DEFAULT_RELAY)   : _cfgBuf;
            if (_cfgSel == CF_CHANNEL) _channel = _cfgBuf.isEmpty() ? String(DEFAULT_CHANNEL) : _cfgBuf;
            if (_cfgSel == CF_NAME)    _name    = _cfgBuf;
            _saveConfig();
            _cfgEditing = false; _cfgBuf = "";
            _cfgStatus = "Saved"; _cfgStatusOk = true;
            if (nostrClient.isConnected()) {
                nostrClient.subscribe(_channel);
                _lastRefreshMs = millis();
            }
            _needsRedraw = true; return;
        }
        if (ki.ch && (int)_cfgBuf.length() < 100) { _cfgBuf += ki.ch; _cfgStatus = ""; _needsRedraw = true; }
        return;
    }

    if (ki.arrowLeft)  { _page = 1; _needsRedraw = true; return; }
    if (ki.arrowRight) { _page = 0; _needsRedraw = true; return; }
    if (ki.arrowUp || ki.arrowDown) {
        _cfgSel = (CfgField)(((int)_cfgSel + 1) % CF_COUNT);
        _cfgStatus = ""; _needsRedraw = true; return;
    }
    if (ki.enter) {
        if      (_cfgSel == CF_RELAY)   _cfgBuf = _relay;
        else if (_cfgSel == CF_CHANNEL) _cfgBuf = _channel;
        else if (_cfgSel == CF_NAME)    _cfgBuf = _name;
        _cfgEditing = true; _cfgStatus = ""; _needsRedraw = true;
    }
}

// ---- Lifecycle --------------------------------------------------------------

void AppNostrProx::onEnter() {
    _needsRedraw  = true;
    _p0Status     = "";
    _p0GenConfirm = false;
    _feedScroll       = 0;
    _feedScrollPinned = true;
    _compBuf      = "";
    _compEditing  = false;
    _compStatus   = "";
    _pendingRelayCheck = false;
    _cfgSel       = CF_RELAY;
    _cfgEditing   = false;
    _cfgBuf       = "";
    _cfgStatus    = "";
    _keysLoaded   = false;
    _reloadKeys();
    _lastPollMs    = 0;
    _lastRefreshMs = 0;
    _autoConnect   = false;

    // Always open on the feed
    _page = 0;

    // Queue auto-connect: fires on first onUpdate tick if conditions are met
    if (!nostrClient.isConnected() && !credStoreLocked &&
        WiFi.status() == WL_CONNECTED) {
        _autoConnect = true;
    }
}

void AppNostrProx::onExit() {
    // Disconnect cleanly — no relay traffic while app is closed
    nostrClient.disconnect();
    _compEditing  = false;
    _cfgEditing   = false;
    _p0GenConfirm = false;
}

void AppNostrProx::onUpdate() {
    unsigned long now = millis();

    if (M5Cardputer.BtnA.wasPressed()) {
        uiManager.notifyInteraction();
        _compEditing = false; _cfgEditing = false; _p0GenConfirm = false;
        _page = (_page + 1) % NUM_PAGES;
        _needsRedraw = true;
        return;
    }

    // Auto-connect on first tick after entering the app
    if (_autoConnect) {
        _autoConnect = false;
        _p0Status = "Connecting..."; _p0StatusOk = true;
        _needsRedraw = true;
        _drawPage0(); // show "connecting..." before blocking call
        if (nostrClient.connect(_relay)) {
            nostrClient.subscribe(_channel);
            _lastRefreshMs = millis();
            _p0Status = ""; // clear on success — feed speaks for itself
        } else {
            _p0Status = nostrClient.lastError();
            _p0StatusOk = false;
        }
        _needsRedraw = true;
    }

    // Poll for incoming frames every 50ms
    if (now - _lastPollMs >= 50) {
        _lastPollMs = now;
        bool got = nostrClient.poll();

        // One-shot relay OK/REJECT check after sending
        if (_pendingRelayCheck && !nostrClient.lastRelayMsg().isEmpty()) {
            const String& rm = nostrClient.lastRelayMsg();
            if (rm.startsWith("REJECT")) {
                _compStatus   = rm.length() > 28 ? rm.substring(0, 27) + "~" : rm;
                _compStatusOk = false;
                got = true;
            }
            _pendingRelayCheck = false;
        }

        if (got) _needsRedraw = true;
    }

    // Auto-refresh subscription every 60s while connected.
    if (nostrClient.isConnected() && _lastRefreshMs > 0 &&
        now - _lastRefreshMs >= 60000UL) {
        nostrClient.subscribe(_channel);
        _lastRefreshMs = now;
        _needsRedraw = true;
    }

    if (_needsRedraw) {
        switch (_page) {
            case 0: _drawPage0(); break;
            case 1: _drawPage1(); break;
            case 2: _drawPage2(); break;
        }
        _needsRedraw  = false;
        _lastRedrawMs = now;
    }

    bool editing = _compEditing || _cfgEditing;
    KeyInput ki  = pollKeys(editing);
    if (!ki.anyKey) return;
    uiManager.notifyInteraction();

    if (ki.esc && !editing && !_p0GenConfirm) {
        uiManager.returnToLauncher(); return;
    }

    switch (_page) {
        case 0: _handlePage0(ki); break;
        case 1: _handlePage1(ki); break;
        case 2: _handlePage2(ki); break;
    }
}

